    private void FillTables(JTable Table, ArrayList<Cancion> Songs) {
        DefaultTableModel TabModel = (DefaultTableModel) Table.getModel();
        Object Line[] = {(Object) Songs.get(Songs.size() - 1).getNombre(), (Object) Songs.get(Songs.size() - 1).getArtista(), (Object) Songs.get(Songs.size() - 1).getAlbum(), (Object) Songs.get(Songs.size() - 1).getPuntos(), (Object) Songs.get(Songs.size() - 1).getAño()};
        TabModel.addRow(Line);
    }

    private void JB_SaveSongActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JB_SaveSongActionPerformed
        try {
            if (JT_NombreSong.equals("") || JT_Album.equals("") || JT_Artista.equals("")) {
                JOptionPane.showMessageDialog(null, "Error, Complete la información.");
            } else {
                Songss.WriteFile(JT_NombreSong.getText(), JT_Artista.getText(), JT_Album.getText(), Integer.parseInt(JFT_Año.getText()), JCB_Puntos.getSelectedIndex() + 1);
                Songs.add(new Cancion(JT_NombreSong.getText(), JT_Artista.getText(), JT_Album.getText(), Integer.parseInt(JFT_Año.getText()), JCB_Puntos.getSelectedIndex() + 1));
                FillTables(JTa_AllSongs, Songs);
                FillTables(JTa_AllSongs2, Songs);
                resetPersona();
                JOptionPane.showMessageDialog(this, "Guardado Exitoso.");
                resetPersona();
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error.");
        }
    }//GEN-LAST:event_JB_SaveSongActionPerformed

