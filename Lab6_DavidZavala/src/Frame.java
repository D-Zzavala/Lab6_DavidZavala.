    private void TablesInit(boolean flag) {
        if (flag == false) {
            File archivo = null;
            Scanner sc = null;
            DefaultTableModel TableModel = (DefaultTableModel) JTa_AllSongs.getModel();
            DefaultTableModel TableModel2 = (DefaultTableModel) JTa_AllSongs2.getModel();
            try {
                archivo = new File("./Files/Canciones/Canciones.txt");
                sc = new Scanner(archivo);
                while (sc.hasNext()) {
                    String[] Next = sc.nextLine().split("\\|");
                    String Nom = Next[0];
                    String Art = Next[1];
                    String Alb = Next[2];
                    String Punt = Next[3];
                    String Ano = Next[4];
                    Object[] Line = {Nom, Art, Alb, Punt, Ano};
                    Songs.add(new Cancion(Nom, Art, Alb, Integer.parseInt(Punt), Integer.parseInt(Ano)));
                    TableModel.addRow(Line);
                    TableModel2.addRow(Line);
                    JTa_AllSongs.setModel(TableModel);
                    JTa_AllSongs2.setModel(TableModel2);
                }
            } catch (Exception e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(null, "Error en el Reader.");
            }
            sc.close();
        }
        Flag = true;
    }

