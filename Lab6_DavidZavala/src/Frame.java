    private void Song2PlaylistTable() {
        int[] SelectedLine = JTa_AllSongs2.getSelectedRows();
        for (int i = 0; i < SelectedLine.length; i++) {
            Object[] Line = new Object[]{JTa_AllSongs2.getValueAt(SelectedLine[i], 0), JTa_AllSongs2.getValueAt(SelectedLine[i], 1), JTa_AllSongs2.getValueAt(SelectedLine[i], 2), JTa_AllSongs2.getValueAt(SelectedLine[i], 3), JTa_AllSongs2.getValueAt(SelectedLine[i], 4)};
            DefaultTableModel model = (DefaultTableModel) JTa_SongsinPlaylist.getModel();
            model.addRow(Line);
        }
    }

    private void Song2PlaylistsArrayList(ArrayList<Cancion> Songs) {
        TempSongs.removeAll(Songs);
        int[] SelectedLine = JTa_AllSongs2.getSelectedRows();
        for (int i = 0; i < SelectedLine.length; i++) {
            String Nom = (String) JTa_AllSongs2.getValueAt(SelectedLine[i], 0);
            String Art = (String) JTa_AllSongs2.getValueAt(SelectedLine[i], 1);
            String Alb = (String) JTa_AllSongs2.getValueAt(SelectedLine[i], 2);
            int Pun = Integer.parseInt(JTa_AllSongs2.getValueAt(SelectedLine[i], 3).toString());
            int Ano = Integer.parseInt(JTa_AllSongs2.getValueAt(SelectedLine[i], 4).toString());
            Cancion Song = new Cancion(Nom, Art, Alb, Pun, Ano);
            System.out.println(Song);
            Songs.add(Song);
        }
        JB_Song2Playlist.setVisible(false);
    }

